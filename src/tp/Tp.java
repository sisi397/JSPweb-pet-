package tp;

public class Tp {
	
	private int tpID;
	private String tpTitle;
	private String userID;
	private String tpDate;
	private String tpContent;
	private int tpAvailable;
	
	public int getTpID() {
		return tpID;
	}
	public void setTpID(int tpID) {
		this.tpID = tpID;
	}
	public String getTpTitle() {
		return tpTitle;
	}
	public void setTpTitle(String tpTitle) {
		this.tpTitle = tpTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getTpDate() {
		return tpDate;
	}
	public void setTpDate(String tpDate) {
		this.tpDate = tpDate;
	}
	public String getTpContent() {
		return tpContent;
	}
	public void setTpContent(String tpContent) {
		this.tpContent = tpContent;
	}
	public int getTpAvailable() {
		return tpAvailable;
	}
	public void setTpAvailable(int tpAvailable) {
		this.tpAvailable = tpAvailable;
	}
	
	
	
	

}
